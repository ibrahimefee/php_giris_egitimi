PHP kodlarının çalışma mantığı:

    - Bir web tarayıcısı, PHP kodlarını içeren bir web sayfasını sunucuya gönderir.

    - Web sunucusu, PHP kodlarını yorumlamak ve işlemek için bir PHP yorumlayıcısı kullanır.

    - PHP yorumlayıcısı, kodları işlemek için sunucuda bir işlem başlatır. 
    Bu işlem, sunucu tarafında çalışan bir PHP betiği olarak bilinir.

    - PHP betiği, kodları okur, yorumlar ve sonuçları HTML kodlarına dönüştürür.

    - HTML kodları, web sunucusu tarafından web tarayıcısına gönderilir.

    - Web tarayıcısı, aldığı HTML kodlarını kullanarak, sonuç olarak web sayfasını kullanıcıya gösterir.

    -- Bu süreçte, PHP kodlarının işleyebileceği verilerin kaynakları şunlar olabilir:

        - HTML formları: Kullanıcılar tarafından sunucuya gönderilen verileri işlemek için kullanılabilir.

        - Veritabanları: PHP, veritabanlarına bağlanarak, verileri okuyabilir ve yazabilir.

        - Dosyalar: PHP, sunucuda bulunan dosyaları okuyabilir ve yazabilir.

        - Diğer web servisleri: PHP, diğer web servislerine (örneğin SOAP veya RESTful servisleri) bağlanarak, veri alışverişi yapabilir.

